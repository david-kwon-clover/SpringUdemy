
public class Main {
  public static void main(String[] args) {
    // DECLARE VARIABLES
    int age;
    String name;

    // INITIALIZE VARIABLES WITH VALUES
    age = 27;
    name = "David";

    // DECLARING AND INITIALIZING ON ONE LINE
    boolean isHungry = true;

    // REASSIGNMENT
    age = 28;

    // PRINT VARIABLES AND DESCRIPTIONS
    System.out.println("age variable: " + age);
    System.out.println("name variable: " + name);
    System.out.println("isHungry variable: " + isHungry);
  }
}